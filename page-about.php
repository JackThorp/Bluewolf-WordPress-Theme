          <h1>The One</h1>
          <div class="text-center">
            <p>The simple lesson from the short history of online platforms is that “the winner takes all”. Amazon, ebay, Uber, Airbnb etc backed by huge sums of money have not only been disruptive to old business but have become dominant in the new.  If ethical, fair, cooperative consumption is to counter the market power of these giants the “ethical community” - those of us who believe business can be done differently - must act together to create a single, trading space that becomes the online home of the Good.  </p>
            <p>~</p>
            <p>It is difficult to see how myriad separate sites will ever become sufficiently visible in mainstream consumer markets to challenge the terms on which our daily needs are fulfilled.  By working together through a cooperative platform many individual sellers and customers can aggregate the demand for ethical goods, assemble capital to build the marketplace and give coherence to a consumer movement that puts people and planet before profit.</p>
